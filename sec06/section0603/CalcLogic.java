package sec06.section0603;

/**
 * CalcLogic
 */
public class CalcLogic {
    /**
     * 
     * @param a 1つ目の整数
     * @param b 2つ目の整数
     * @return 足し算の結果
     */
    public static int tasu(int a,int b) {
        return (a+b);
    }

    /**
     * 
     * @param a 1つ目の整数
     * @param b 2つ目の整数
     * @return 引き算の結果
     */
    public static int hiku(int a,int b) {
        return (a-b);
    }
}